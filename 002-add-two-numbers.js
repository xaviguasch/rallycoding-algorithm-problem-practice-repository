// Add Two Numbers
// You are given two integers as arguments. Add the arguments together and return the sum.

// Requirements
// Must return a single integer
// Must work with negative integers

// Example #1
// solve(10, 10)
// > 20

// Example #2
// solve(-90, -90)
// > -180

const solve = (intOne, intTwo) => {
  return intOne + intTwo
}

